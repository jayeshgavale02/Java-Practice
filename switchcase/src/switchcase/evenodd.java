public class evenodd {
	public static void main(String[] args) {
    
		
		int number = 47;
		
		int res = number%2;

		switch(res) {
		case 0:
			System.out.println("This is Even number");
		 break;
		case 1:
			System.out.println("This is Odd number");

		}
		
		
	}
	
}
